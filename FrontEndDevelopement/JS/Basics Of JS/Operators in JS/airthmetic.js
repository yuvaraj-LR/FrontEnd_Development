let num1 = 10;
let num2 = 20;

// Sum of Two numbers
console.log(num1 + " + " + num2 + " = " + (num1 + num2));

// Difference of Two numbers
console.log(num1 + " - " + num2 + " = " + (num1 - num2));

// Product of Two numbers
console.log(num1 + " * " + num2 + " = " + (num1 * num2));

// Division of Two numbers
console.log(num1 + " / " + num2 + " = " + (num1 / num2));

// Modulus of Two numbers
console.log(num1 + " % " + num2 + " = " + (num1 % num2));

// Power of the numbers
console.log(num1 + " ** " + num2 + " = " + (num1 ** num2));

// Increment
console.log("Increment Before : " + ++num1);
console.log("Increment After : " + num1++);

// Decrement
console.log("Decrement Before : " + --num1);
console.log("Decrement After : " + num1--);