num1 = 5;
num2 = 5;
num3 = 6;
number1 = "5";

// Strict equaltiy operator ( === ) ( !== )
// Datatype must be equal.

console.log(num1 === num2); //number === number
console.log(num1 === number1); //String === number
console.log(num1 !== num3); // number !== number

// Lose equaltiy operator ( == )
// Datatype may or may not equal.

console.log(num1 == num2);
console.log(num1 == number1);
console.log(num1 != num3); // number != number