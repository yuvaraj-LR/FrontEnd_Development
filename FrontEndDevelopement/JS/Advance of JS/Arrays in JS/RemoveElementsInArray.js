// Removing the element in the array.

const colors = ['red', 'blue', 'green'];
console.log(colors);

// Remove the starting element in the array.
colors.pop();
console.log(colors);

// Remove the Ending element in the array.
colors.shift();
console.log(colors);

const colors1 = ['red', 'blue', 'green'];
// Removing a element in the particular index.
colors1.splice(2, 1);
console.log(colors1);